public class ejemplolong {
    public static void main(String[] args) {
        long a = 1000000000L; //Un número grande
        long b = 2000000000L;
//Suma
        long suma = a + b;

// Multiplicación
        long producto = a * b;
        System.out.println("Suma (long): " + suma);
        System.out.println("Multiplicación (long): " + producto);
    }
}